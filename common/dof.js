// Calculate all depth of field numbers
function calculateDof(distance, camera_id, fstop_or_aperture, focal_id_or_focal, units, is_portrait)  {

    var CoC = CIRCLES_OF_CONFUSION[camera_id] || CIRCLES_OF_CONFUSION['$'],
        aperture = typeof fstop_or_aperture === "string" ? F_STOPS[fstop_or_aperture] : fstop_or_aperture,
        focal = typeof focal_id_or_focal === "string" ? FOCAL_LENGTHS[focal_id_or_focal] : focal_id_or_focal;

    units = units || 1;
    is_portrait = is_portrait || false;

	// calculate hyperfocal and near distance
	var hyperFocal = (focal * focal) / (aperture * CoC) + focal,
        dofNear = 0,
        dofFar = 0,
        dofNearPercent = 0,
        dofFarPercent = 0,
        _1K = 1e3,
        divider = _1K * units;

    distance = distance * _1K * units; // convert to millimeters
    dofNear = ((hyperFocal - focal) * distance) / (hyperFocal + distance - (2 * focal));

    // Prevent 'divide by zero' when calculating far distance.
    dofFar = (hyperFocal - distance) < 1e-5 ? 1e7 : ((hyperFocal - focal) * distance) / (hyperFocal - distance);

    // Calculate percentage of DOF in front and behind the subject.
    dofNearPercent = (distance - dofNear) / (dofFar - dofNear) * 100;
    dofFarPercent = (dofFar - distance) / (dofFar - dofNear) * 100;

    return {
        hf: hyperFocal / divider,
        dn: dofNear / divider,
        df: dofFar / divider,
        dt: (dofFar - dofNear)/ divider,
        pdn: Math.round(dofNearPercent),
        pdf: Math.round(dofFarPercent),
        fw: calculateFrameSize(distance / divider, camera_id, focal, units, !is_portrait),
        fh: calculateFrameSize(distance / divider, camera_id, focal, units, is_portrait),
        s: distance / divider
    };
}

function calculateDistance(target_height, camera, focal, units, is_portrait) {
    units = units || 1;
    is_portrait = is_portrait || false;
    target_height = target_height / (1000 * units);
    var sensor_dimensions = SENSOR_SIZES[camera] || SENSOR_SIZES["$"];
    return focal * target_height / sensor_dimensions[is_portrait ? 0 : 1] * 1000 * units;
}

function calculateFrameSize(distance, camera, focal, units, is_portrait_or_width) {
    units = units || 1;
    is_portrait_or_width = is_portrait_or_width || false;
    distance = distance / (1000 * units);
    var sensor_dimensions = SENSOR_SIZES[camera] || SENSOR_SIZES["$"];
    return distance * sensor_dimensions[is_portrait_or_width ? 0 : 1] / focal * 1000 * units;
}

